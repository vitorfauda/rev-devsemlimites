/** Retorna lista ordenada com populares primeiro */
export function getBanksList(): Bank[] {
  const popular = BANKS.filter(b => b.popular);
  const rest = BANKS.filter(b => !b.popular).sort((a, b) => a.name.localeCompare(b.name, "pt-BR"));
  return [...popular, ...rest];
}

/** Procura banco por código */
export function findBankByCode(code: string): Bank | undefined {
  const padded = String(code).padStart(3, "0");
  return BANKS.find(b => b.code === padded);
}

/** Filtra por nome ou código (busca case-insensitive) */
export function searchBanks(query: string): Bank[] {
  if (!query) return getBanksList();
  const q = query.toLowerCase().trim();
  return BANKS.filter(b =>
    b.name.toLowerCase().includes(q) || b.code.includes(q)
  );
}
